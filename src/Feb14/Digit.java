package Feb14;

public class Digit {
    private String letterValue;
    private int numValue;

    public Digit(String letterValue, int numValue) {
        this.letterValue = letterValue;
        this.numValue = numValue;
    }

    public String getLetterValue() {
        return letterValue;
    }

    public int getNumValue() {
        return numValue;
    }
}
